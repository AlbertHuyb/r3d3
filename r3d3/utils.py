def pose_matrix_to_quaternion(matrix: torch.Tensor) -> torch.Tensor:
    """ Converts matrix pose into translation and quaternions
    Args:
        Pose matrix of shape [..., 4, 4]
    Returns:
        quat: Pose as [x, y, z, quaternions]
    """
    quat = torch.cat((
        matrix[..., 0:3, 3],
        matrix_to_quaternion(matrix[..., 0:3, 0:3])
    ), dim=-1)[..., [0, 1, 2, 4, 5, 6, 3]]
    return quat


def pose_quaternion_to_matrix(quat: torch.Tensor) -> torch.Tensor:
    """ Converts poses in [..., 7] format into matrices of shape [..., 4, 4]
    Args:
        quat: Pose as [x, y, z, quaternions]
    Returns:
        Pose matrix of shape [..., 4, 4]
    """
    matrix = torch.zeros(size=(*quat.shape[:-1], 4, 4), device=quat.device, dtype=quat.dtype)
    matrix[..., 0:3, 0:3] = quaternion_to_matrix(quat[..., [6, 3, 4, 5]])
    matrix[..., 0:3, 3] = quat[..., :3]
    matrix[..., 3, 3] = 1
    return matrix
